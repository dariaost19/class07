package class7;

public class ForLoops3 {
    //write a table 5x (multiplication)
    public static void main(String[] args) {
        for (int i = 1; i <=10 ; i++) {
            System.out.println("5 *" +i+ "=" +(5*i));

        }
        // table for 3 x
        for (int b = 0; b <=10 ; b++) {
            System.out.println("3 * " +b+ "=" +(3*b));

        }
    }
}
